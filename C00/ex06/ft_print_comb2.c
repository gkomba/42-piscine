#include <unistd.h>

void	write_comb2(int v)
{
	char	k;

	if (v < 10)
	{
		k = v + 48;
		write(1, "0", 1);
		write(1, &k, 1);
	}
	else
	{
		k = (v / 10) + 48;
		write(1, &k, 1);
		k = v % 10 + 48;
		write(1, &k, 1);
	}
}

void	ft_print_comb2(void)
{
	int	v[2];

	v[0] = 0;
	while (v[0] < 99)
	{
		v[1] = v[0] + 1;
		while (v[1] < 100)
		{
			write_comb2(v[0]);
			write(1, " ", 1);
			write_comb2(v[1]);
			if (!(v[0] == 98 && v[1] == 99))
				write(1, ", ", 2);
			v[1]++;
		}
		v[0]++;
	}
}
