#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char		*d;
	char		*dest;
	int			tam;

	tam = ft_strlen(src);
	d = ((dest = (char *)malloc(tam * sizeof(char) + 1)));
	if (!d)
		return (0);
	ft_strcpy(dest, src);
	return (dest);
}
