#include <unistd.h>

void	put_char(char c)
{
	write(1, &c, 1);
}

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = ac - 1;
	while (i > 0)
	{
		j = 0;
		while (av[i][j])
		{
			put_char(av[i][j]);
			j++;
		}
		put_char('\n');
		i--;
	}
	return (0);
}
